
/**
 * Indica cuanto falta para el inicio de  la primavera.
 * 
 * @author (Mauricio) 
 * @version (1.0)
 */
public class InicioDeLaPrimavera
{
    public int calcularTiempoRestante(int horaActual){
        int respuesta;
        respuesta = 24 + (24 - horaActual);
        return respuesta;
    }
}
